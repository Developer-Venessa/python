# arithmetic operators
x=34
y=6
print(f"addition operator {x+y}")
print(f"substraction operator {x-y}")
print(f"multiplication operator {x*y}")
print(f"division operator {x/y}")
print(f"remainder operator {x%y}")

# comparison operators
print(f"{x} is not equal to {y}:", x != y)
print(f"{x} is equal to {y}:", x == y)
print(f"{x} greater than to {y}:", x > y)
print(f"{x} is less than to {y}:", x < y)
print(f"{x} is greater than or equals to {y}:", x >= y)
print(f"{x} is less than or equals to {y}:", x <= y)

# logical operator
print('Is this statement true?:',x>y and x<y)
print('Is any of this statement true?:',x>30 or x<5)


